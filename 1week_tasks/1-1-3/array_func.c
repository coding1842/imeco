/*------------------------------------------------ ---------------------------------
* 기능 : array
* 설명 : 문자열 출력
* 입력 매개변수 : piMax - 상수열, piMin - 상수열, piArr[] - 상수열
* 출력 매개변수:
* 반환값 :
*-------------------------------------- -------------------*/

void array(int* piMax, int* piMin, int piArr[])
{
    int i;
    for (i = 0; i < 5; i++)
    {
        if (*piMax <= piArr[i])
        {
            *piMax = piArr[i];
        }

        if (*piMin >= piArr[i])
        {
            *piMin = piArr[i];
        }
    }
}  /* array의 끝 */

